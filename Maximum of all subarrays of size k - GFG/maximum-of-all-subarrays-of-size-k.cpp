class Solution
{
  public:
    //Function to find maximum of each subarray of size k.
    vector <int> max_of_subarrays(int *arr, int n, int k)
    {
        // your code here
        vector<int>ans;
        multiset<int>s1;int j=0;
        for(int i=0;i<n;i++)
        {
            s1.insert(arr[i]);
            if(s1.size()>=k)
            {
                ans.push_back(*(--s1.end()));
                s1.erase(s1.find(arr[j]));
                j++;
            }
        }
        return ans;
    }
};

